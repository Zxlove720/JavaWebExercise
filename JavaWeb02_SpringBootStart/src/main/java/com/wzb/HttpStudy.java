public class HttpStudy {

        /*
        HTTP协议：Hyper Text Transfer Protocol，超文本传输协议，规定了浏览器和服务器之间数据传输的规则（格式）
        浏览器给服务器发送请求，服务器处理之后响应对应的数据
        服务器需要解析浏览器的数据；浏览器也需要解析服务器响应的数据，为了成功解析，就通过HTTP协议规定了数据传输的格式

        HTTP特点：
            1.基于TCP协议：面向连接，安全（三次握手）
            2.基于请求-响应模型：一次请求对应一次响应
            3.HTTP协议是无状态协议（对于事务处理没有记忆能力，每次请求-响应都是独立的）
                缺点：多次请求之间不能共享数据（可以通过会话技术解决）
                优点：不共享数据，请求响应的速度更快

         */

}
